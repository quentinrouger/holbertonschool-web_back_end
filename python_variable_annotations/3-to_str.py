#!/usr/bin/env python3
"""
This module contains a function that takes a float n as argument
and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    This function takes a float n as argument and returns the string
    representation of the float.

    Parameters:
    n (float): the float to be string representated.

    Return:
    str: the  string representation of the float.
    """
    return str(n)
