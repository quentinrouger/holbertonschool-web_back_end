#!/usr/bin/env python3
"""This module contains a function that adds two floats"""

def add(a: float, b: float) -> float:
    """
    This function adds two floats and returns the sum as a float
    
    Parameters:
    a: first float
    b: second float
    
    Return: 
    float: the sum of a and b
    """
    return a + b
