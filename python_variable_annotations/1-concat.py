#!/usr/bin/env python3
"""This module contains a function that concatenates two strings."""


def concat(str1: str, str2: str) -> str:
    """
    This function concatenates two strings and returns the result as a string.

    Parameters:
    str1 (str): first string.
    str2 (str): second string.

    Return:
    str: the concatenation of str1 and str2.
    """
    return str1 + str2
